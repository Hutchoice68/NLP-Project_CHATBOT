class NoiseInjector:
    """Add realistic noise to text"""
    
    @staticmethod
    def add_spacing_noise(text, prob=0.15):
        """Add spacing issues"""
        if random.random() > prob:
            return text
        
        # Extra spaces
        if random.random() < 0.5:
            text = re.sub(r'\s', '  ', text, count=random.randint(1, 3))
        # Remove spaces
        else:
            words = text.split()
            if len(words) > 2:
                pos = random.randint(0, len(words)-2)
                words[pos] = words[pos] + words[pos+1]
                del words[pos+1]
                text = ' '.join(words)
        
        return text
    
    @staticmethod
    def add_punctuation_noise(text, prob=0.2):
        """Add punctuation"""
        if random.random() > prob:
            return text
        
        puncs = ["...", "!!", "???", "!?", ".", ",", ""]
        return text + random.choice(puncs)
    
    @staticmethod
    def add_repetition(text, prob=0.1):
        """Repeat words (excitement)"""
        if random.random() > prob:
            return text
        
        words = text.split()
        if len(words) > 0:
            word_to_repeat = random.choice(words)
            text = text.replace(word_to_repeat, f"{word_to_repeat} {word_to_repeat}", 1)
        
        return text
    
    @staticmethod
    def add_case_mixing(text, prob=0.1):
        """Mix uppercase/lowercase (for English)"""
        if random.random() > prob:
            return text
        
        chars = list(text)
        for i in range(len(chars)):
            if chars[i].isalpha() and random.random() < 0.3:
                chars[i] = chars[i].upper() if chars[i].islower() else chars[i].lower()
        
        return ''.join(chars)


