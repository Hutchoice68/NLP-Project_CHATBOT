class TemplateSampler:
    """Sample templates with length control"""
    
    @staticmethod
    def sample_by_length(intent_templates, length_distribution=None):
        """Sample with specific length distribution"""
        
        if length_distribution is None:
            # Balanced distribution
            length_distribution = {
                "ultra_short": 0.10,
                "short": 0.25,
                "medium": 0.30,
                "long": 0.25,
                "ultra_long": 0.10
            }
        
        # Select length category
        length_cat = random.choices(
            list(length_distribution.keys()),
            weights=list(length_distribution.values())
        )[0]
        
        # Get templates for that length
        if length_cat in intent_templates:
            templates = intent_templates[length_cat]
        else:
            # Fallback to medium
            templates = intent_templates.get("medium", intent_templates.get("short", [""]))
        
        # Handle "with_context" separately
        if random.random() < 0.2 and "with_context" in intent_templates:
            templates = intent_templates["with_context"]
        
        if not templates:
            return ""
        
        return random.choice(templates)
    
