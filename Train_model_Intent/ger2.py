# 6. MAIN DATASET GENERATOR
# ============================================================================

def generate_diverse_dataset(
    target_total=40000,
    length_distribution=None,
    include_multi_intent=True,
    include_noise=True,
    style_distribution=None,
    save_path="Data/intents_dataset_diverse.csv"
):
    """
    Generate highly diverse Thai intent dataset
    
    Args:
        target_total: Total number of samples
        length_distribution: Dict of length category probabilities
        include_multi_intent: Include multi-intent samples
        include_noise: Add realistic noise
        style_distribution: Dict of style probabilities
        save_path: Output file path
    """
    
    print("="*80)
    print("ðŸš€ GENERATING DIVERSE THAI INTENT DATASET")
    print("="*80)
    
    # Initialize
    decorator = AdvancedDecorator()
    sampler = TemplateSampler()
    noise = NoiseInjector()
    
    # Default distributions
    if length_distribution is None:
        length_distribution = {
            "ultra_short": 0.10,
            "short": 0.25,
            "medium": 0.30,
            "long": 0.25,
            "ultra_long": 0.10
        }
    
    if style_distribution is None:
        style_distribution = {
            "ultra_polite": 0.15,
            "polite": 0.30,
            "casual": 0.30,
            "direct": 0.15,
            "question": 0.05,
            "emoji_heavy": 0.05
        }
    
    # Calculate samples per intent
    num_intents = len(intents_expanded)
    
    # Reserve samples for multi-intent and extra unknown
    multi_intent_samples = 500 if include_multi_intent else 0
    extra_unknown = 1000
    
    regular_samples = target_total - multi_intent_samples - extra_unknown
    samples_per_intent = regular_samples // num_intents
    
    print(f"\nðŸ“Š Distribution Plan:")
    print(f"   Regular intents: {regular_samples} ({samples_per_intent} per intent)")
    print(f"   Multi-intent: {multi_intent_samples}")
    print(f"   Extra unknown: {extra_unknown}")
    print(f"   Total: {target_total}\n")
    
    # Generate samples
    rows = []
    platforms = ["LINE", "Facebook", "Web", "Instagram", "Mobile App"]
    
    # Regular intents
    for intent, templates in intents_expanded.items():
        print(f"ðŸ“ Generating {intent}...")
        
        for i in range(samples_per_intent):
            # Sample base text with length control
            base_text = sampler.sample_by_length(templates, length_distribution)
            
            if not base_text:
                continue
            
            # Select style
            style = random.choices(
                list(style_distribution.keys()),
                weights=list(style_distribution.values())
            )[0]
            
            # Decorate
            text = decorator.decorate_by_style(base_text, intent, style)
            
            # Add noise
            if include_noise:
                if random.random() < 0.15:
                    text = noise.add_spacing_noise(text)
                if random.random() < 0.20:
                    text = noise.add_punctuation_noise(text)
                if random.random() < 0.08:
                    text = noise.add_repetition(text)
                if random.random() < 0.10:
                    text = noise.add_case_mixing(text)
            
            rows.append({
                "text": text,
                "intent": intent,
                "platform": random.choice(platforms),
                "style": style,
                "word_count": len(text.split()),
                "char_count": len(text)
            })
    
    # Multi-intent samples
    if include_multi_intent:
        print(f"\nðŸ”€ Generating multi-intent samples...")
        multi_samples = sampler.create_multi_intent(intents_expanded, multi_intent_samples)
        
        for sample in multi_samples:
            text = decorator.decorate_by_style(
                sample["text"], 
                sample["primary_intent"], 
                "random"
            )
            rows.append({
                "text": text,
                "intent": sample["intent"],
                "platform": random.choice(platforms),
                "style": "multi",
                "word_count": len(text.split()),
                "char_count": len(text)
            })
    
    # Extra unknown samples
    print(f"\nâ“ Generating extra unknown samples...")
    unknown_samples = generate_unknown_samples(extra_unknown)
    for unk_text in unknown_samples:
        rows.append({
            "text": unk_text,
            "intent": "unknown",
            "platform": random.choice(platforms),
            "style": "unknown",
            "word_count": len(unk_text.split()),
            "char_count": len(unk_text)
        })
    
    # Create DataFrame
    df = pd.DataFrame(rows)
    df = df.sample(frac=1, random_state=42).reset_index(drop=True)
    
    # Save
    import os
    os.makedirs(os.path.dirname(save_path), exist_ok=True)
    df.to_csv(save_path, index=False, encoding="utf-8-sig")
    
    # Summary
    print("\n" + "="*80)
    print("âœ… DATASET GENERATED SUCCESSFULLY!")
    print("="*80)
    print(f"\nðŸ“ Saved to: {save_path}")
    print(f"ðŸ“Š Total samples: {len(df):,}")
    print(f"\nðŸ“‹ Intent Distribution:")
    print(df['intent'].value_counts().to_string())
    
    print(f"\nðŸ“ Length Statistics:")
    print(f"   Words: {df['word_count'].min()}-{df['word_count'].max()} "
          f"(mean: {df['word_count'].mean():.1f}, std: {df['word_count'].std():.1f})")
    print(f"   Chars: {df['char_count'].min()}-{df['char_count'].max()} "
          f"(mean: {df['char_count'].mean():.1f})")
    
    if 'style' in df.columns:
        print(f"\nðŸŽ¨ Style Distribution:")
        print(df['style'].value_counts().to_string())
    
    print(f"\nðŸ“± Platform Distribution:")
    print(df['platform'].value_counts().to_string())
    
    # Show examples
    print(f"\nðŸ” SAMPLE TEXTS (Random 10):\n")
    for idx, row in df.sample(10).iterrows():
        print(f"[{row['intent']}] ({row['word_count']} words)")
        print(f"   {row['text']}\n")
    
    print("="*80)
    
    return df


